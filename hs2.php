<h2>London Birmingham Airport?</h2>
<p>Royal Assent just given the go-ahead on building HS2.  This will be the second high-speed rail ine with phase one linking Birmingham with London.  With the proposed Birmingham Interchange station to serve the airport and NEC this will put the capital within 38 minutes of BHX.  To put that into some context, the Gatwick Express service running from London's Victoria station to Gatwick takes 30 minutes. Even more interesting is that it currently takes 48 minutes on the Stanstead Express from Liverpool Street station in central London to Stanstead airport in Essex.  Considering some of the more competitively priced flights out of Birmingham, this could tempt people to swap London's largely congested airports for Birmingham International.</p>
