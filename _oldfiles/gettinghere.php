<h3>Birmingham Airport Postcode</h3>
<p>If you're driving and will be using a Sat Nav, you can use postcode <strong>B26 3QJ</strong>.</p>
<h3>From the North West</h3>
<p>Take the M6 South, and at Junction 4a, take the M42 south.  Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre.  Follow signs to the airport.</p>
<h3>From the North East</h3>
<p>Take the M1 South, and at Junction 23a, take the A42 South. Then at Junction 11, take the M42 South. Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre.  Follow signs to the airport.</p>

<h3>From the South West</h3>
<p>Take the M5 North, and take Junction 4a Take the M42 East.  Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre.  Follow signs to the airport.</p>
<h3>From the South East</h3>
<p>Take the M40 North. At Junction 3a, Take the M42 North.  Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre.  Follow signs to the airport.<br />
<br />
Alternatively, take the M1 North, and at Junction 19 join the M6.  Leave the M6 at Junction 4, and take the M42 South. Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre.  Follow signs to the airport.</p>
<h3>From the South</h3>
<p>Take the A34 North towards Oxford, then join the M40 West at Junction 9, heading towards Birmingham. At Junction 3a, Take the M42 North. Leave the M42 at Junction 6 heading west on the A45 towards Birmingham city centre. Follow signs to the airport.</p>

