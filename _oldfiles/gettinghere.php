<h1>Getting to Birmingham International Airport</h1>

<div id="leftpic"><a href="http://www.flickr.com/photos/cmdrgravy/458993448/" title="M42 Junction 3A by CmdrGravy, on Flickr"><img src="http://farm1.static.flickr.com/253/458993448_34fb47b9dd.jpg" width="243" height="175" alt="M42 Junction 3A" /></a></div>
<p>With Birmingham being on many primary transport routes, getting to the airport is relatively easy.  Birmingham Airport is located approximately 8 Miles east from the city centre just off the A45 Coventry Road, and at Junction 6 of the M42. </p>
<p>Despite Birmingham International being a major airport, the only real traffic congestion in the area is around peak morning and evening rush hours, with most of this confined to the M42 and not the approach roads.  However, that can change if there is a major event on at the NEC, which is very close to the airport.</p>
