<h2>Purple Parking</h2>
<p>Purple Parking offer a very cost effective parking solution for Birmingham Airport.  It is worth noting that they use off-site locations so operate a 'Park and Ride' service.  Be sure to factor in the extra time traveling from the car park to the terminal.</p>
