	<h2>Delayed for Five Hours</h2>
	<p>At this point you don't have to take the flight.  However the choice you make will affect the compenstation.</p>
	<h3>If you take the flight</h3>
	<p>You can claim up to £600 in compensation. Plus the same entitlements for being delayed for two hours.</p>
	<h3>If you don't take the flight</h3>
	<p>The airline are legally obliged to provide the following:</p>
	<ul>
<li>a full refund for the flight</li>
<li>a full refund for other flights from the airline that you won’t use in the same booking, eg an onward or return flight</li>
<li>if you’re part-way through a journey, a flight back to the airport you originally departed from</li>
<li>food and drink</li>
<li>access to phone calls and emails</li>
<li>accommodation if you’re delayed overnight, as well as journeys between the airport and the hotel</li>
</ul>

	<h2>Flight Cancelled</h2>
	<p>In the case where you flight has been completely cancelled the airline has to provide either of the following:</p>
	<ul>
	<li>Full refund of the flight, including any other flights from the airline that you cannot use, e.g. onward and return journeys.</li>
	<li>Seats on a replacement flight to the same destination.</li>
	</ul>
	<p>On top of that, you also have the right to:</p>
	<ul>
	<li>Reasonable costs if the cancellation delays you by more than two hours.</li>
	<li>Compensation of the alternative flight will delay you by more than two hours.</li></ul>
	<h2>Tips and Further Advice</h2>
	<ul class="simple-list">
	<li>You will not receive compensation for delays from weather or even staff strikes. Be careful or airlines using strikes by air traffic control as an excuse.  Be vigilant to see if that really is the case. </li>
	<li>Try and resolve as much as you can at the airport and with the airline staff.</li>
	<li>If you are not offered support at the time, remember to keep your receipts for any out of pocket expenses, but bear in mind you will only be compensated for reasonable costs so no alcohol, or expensive meals and hotels. Keep original copies of tickets.</li>
	<li>To claim compensation, you will have to deal directly with the airline who you booked with, even if the flight was operated by another carrier. </li>
		<li>If your flight was outside of the EU and on a non-EU airline, contact them directly as regulations will be different.</li>
		<li>Do not rely on these regulations alone, always make sure you have adequate travel insurance.</li>
	<li>If you feel you are not getting what you are entitled from the airline, then you might want to contact the <a href="https://www.caa.co.uk/Passengers/Resolving-travel-problems/Delays-cancellations/Making-a-claim/Claiming-for-costs-and-compensation/">Civil Aviation Authority for advice</a>.</li></ul>




