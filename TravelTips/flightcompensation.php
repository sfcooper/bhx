	<p>The airline industry is extremely complex with very tight schedules for both the plane and the crew.  Unfortunately any problem with the aircraft or crew, for instance from a mechanical issue or extreme weather, can easily mess up these schedules and cause a delay in your flight.  Fortunately there are regulations that provide you with assistance at the time, and also potential financial compensation after the event.<br /><br /> If you have flown from either a country in the EU (inc. UK, Iceland, Liechtenstein, Norway or Switzerland) or have flown on a European carrier then you are protected by law.</p>
<div class="text-center">
<img class="img-thumbnail" src="/images/departureboard.jpg" alt="departure board"/>
</div>
	<h2>Delayed for Two Hours</h2>
	<p>This is the minimum time period for the regulations to apply, and for this time length you may find that you've not even been allowed off the aircraft.  However the airline are obliged to provide you with:</p>
	<ul>
	<li>Basic food and drink</li>
	<li>Access to phone calls and email</li>
	<li>Plus accommodation if you're delayed overnight, including transportation between the terminal and hotel.</li>
	</ul>

	<h2>Delayed for Three Hours</h2>
	<p>For this length of delay, you are now eligible for financial compensation. There are different levels of compensation depending on your individual flight:</p>

