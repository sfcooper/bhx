<p>Getting through security can be a stressful task, particularly if you're not a frequent traveller or have young children with you.  We've added some top tips below to make this important process more bearable and quicker.  Also bear in mind some of the busiest periods are during the school holidays, so allow an extra 15 minutes during these times. </p>
	
<ul class="simple-list">
<li>Check in online - If you check in online you can proceed straight to bag drop or the security gates if you just have hand luggage.  Ultimately this will give you more time to get through security.</li><br />
	<li>Make sure you are within the limits for liquids.  You will not be allowed to go through security with anything larger than 100ml. This also applies to gels and creams.  If you have to take these items on your trip, make sure to pack them in your hold luggage.</li><br />
	<li>Remove any laptops, iPads and Kindles from your bag.  The staff might want to test it's a working device so always make sure your device is charged.</li><br />
	<li>Take off your watches, belts and large jewellery, <strong><em>before</em></strong> you get to the front of the queue.  This will help speed up the entire process for everyone.</li><br />
	<li>Place as many small items in coat/jacket pockets as possible.  I personally find this makes picking up your items much quicker.</li><br />
	<li>Only take your shoes off if you're asked.  Yes, if you're wearing big boots, it's very likely you'll be made to remove them, but most of the time you can go straight through.</li><br/>
	<li>If you want to make sure you get through security quickly, you can always choose to pay for access to the Express Lane.  You can book this in advance for £3.50, or turn up on the day and use one of the kiosks for £5.  A nice little benefit is that children under 5 years go free!</li><br/>
</ul>

