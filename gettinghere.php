<h2>By Rail</h2><br />
		  <p>Birmingham Airport has it's own train station at Birmingham International. This station is very well located for both the airport and the NEC, with a free monorail linking the station with the terminal building.<br />
<br />
From Birmingham New Street - take the Virgin Rail service to London Euston. Birmingham International is the next stop, taking about 10 minutes.  You can also take the London Midland service to Coventry from Birmingham New Street, which takes around 20 minutes.</p>
